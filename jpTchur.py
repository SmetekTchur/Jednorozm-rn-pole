#základní pole
array = ["martin","genb","gamba","skibu","mamlas"]
print(array)
#přidání pomocí append
array.append ("vítr")
print(array)
#daní pryč pomocí remove
array.remove ("genb")
print(array)
#vyměnění slova v poli
array [4] = "slunce"
print(array)
#rozšíření pomocí extend
mame = ["dg", "gug"]
array.extend (mame)
print(array)